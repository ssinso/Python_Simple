# 반복문(Loop)
# - 반복적인 작업을 가능하게 해주는 도구
# - list, str, tuple 등 컬렉션 아비의 아이템을 하나씩
#   순회하면서 사용 가능(for)
# - 특정 조건을 만족하는 경우(while)

# 반복횟수 0 => for
# 반복횟수 X => while

# for문 기본문법(list 활용)
